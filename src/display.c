static void display_char(char c)
{
    if (c == '\n') {
        cursor_page++;
        cursor_col = 0;

        if (cursor_page > 7) {
            cursor_page = 0;
        }

        display_set_cursor(cursor_col, cursor_page);
        return;
    }

    // litere mici -> litere mari
    if (c >= 'a' && c <= 'z') {
        c = c - 'a' + 'A';
    }

    // daca nu mai incape pe linie, trecem jos
    if (cursor_col > 122) {
        cursor_col = 0;
        cursor_page++;

        if (cursor_page > 7) {
            cursor_page = 0;
        }

        display_set_cursor(cursor_col, cursor_page);
    }

    if (c < ' ' || c > 'Z') {
        c = ' ';
    }

    uint8_t index = c - ' ';

    for (uint8_t i = 0; i < 5; i++) {
        oled_data(pgm_read_byte(&font5x7[index][i]));
    }

    oled_data(0x00);
    cursor_col += 6;
}

void display_set_cursor(uint8_t col, uint8_t page)
{
    cursor_col = col;
    cursor_page = page;

    oled_command(0xB0 + page);
    oled_command(0x00 + (col & 0x0F));
    oled_command(0x10 + ((col >> 4) & 0x0F));
}

void display_clear(void)
{
    for (uint8_t page = 0; page < 8; page++) {
        display_set_cursor(0, page);
        for (uint8_t col = 0; col < 128; col++) {
            oled_data(0x00);
        }
    }

    display_set_cursor(0, 0);
}

void display_print(const char *str)
{
    while (*str) {
        display_char(*str++);
    }
}


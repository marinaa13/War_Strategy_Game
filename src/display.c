#define OLED_ADDR 0x3C

static uint8_t cursor_col = 0;
static uint8_t cursor_page = 0;

static void oled_command(uint8_t cmd)
{
    twi_start();
    twi_write(OLED_ADDR << 1);
    twi_write(0x00);
    twi_write(cmd);
    twi_stop();
}

static void oled_data(uint8_t data)
{
    twi_start();
    twi_write(OLED_ADDR << 1);
    twi_write(0x40);
    twi_write(data);
    twi_stop();
}


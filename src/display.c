void display_draw_bitmap_128x64(const uint8_t *bitmap)
{
    for (uint8_t page = 0; page < 8; page++) {
        display_set_cursor(0, page);

        for (uint8_t x = 0; x < 128; x++) {
            uint8_t data = 0;

            for (uint8_t bit = 0; bit < 8; bit++) {
                uint8_t y = page * 8 + bit;
                uint16_t byte_index = y * 16 + (x / 8);
                uint8_t byte = pgm_read_byte(&bitmap[byte_index]);

                if (byte & (0x80 >> (x % 8))) {
                    data |= (1 << bit);
                }
            }

            oled_data(data);
        }
    }
}
